number_1 = 4
number_2 = 2

print(number_1 + number_2) #6
print(number_1 - number_2) #2
print(number_1 * number_2) #8
print(number_1 / number_2) #2
print(number_1 % number_2) #0
print(number_1 ** number_2) #16



print(number_1 == number_2) #False
print(number_1 != number_2) #True

